package com.operators;

public class Conversion {

	public static void main(String[] args) {
		int a=11;
		float f=a;
		System.out.println("f="+f);
		
		float f1=43.9f;
		int x=(int)f1;
		System.out.println("x="+x);
		
		double d=17.11;
		int x1=(int)d;
		
		char c='a';
		int x3=c;
		System.out.println("x3="+x3);
		
		int a1=69;
		char c1=(char)a1;
		System.out.println("c1="+c1);

	}

}
